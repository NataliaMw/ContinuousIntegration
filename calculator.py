#small calculator project

#addition
def add(x, y):
    return x + y

#subtraction
def subtract(x, y):
    return x - y

#multiplication
def multiply(x, y):
    return x * y

#division
def divide(x, y):
    return x / y


# use the methods
print(add(2, 3))
print(subtract(2, 3))
print(multiply(2, 3))
print(divide(2, 3))